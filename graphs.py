def general_graph():
    year = 1926
    x_axis = []
    y_axis = []
    for i in range(97):
        x_axis.append(year)
        count = years_and_count.get(str(year))
        if count is None:
            y_axis.append(0)
        else:
            y_axis.append(count)
        year = year + 1
    print(x_axis)
    print(y_axis)
    plt.plot(x_axis, y_axis)
    plt.xlabel('Year')
    plt.ylabel('Number of movies')
    plt.title('Number of animation movies with a female leading character\n that have come out over the years')
    plt.show()


